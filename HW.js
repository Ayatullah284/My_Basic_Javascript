// grade point 
function gradePoint(number) {
    if (number >= 80) {
        console.log('A +');
    }
    else if (number >= 75) {
        console.log('A -');
    }
    else if (number >= 75) {
        console.log('B+');
    }
    else if (number >= 75) {
        console.log('B-');
    }
    else if (number >= 75) {
        console.log('C+');
    }
    else if (number >= 75) {
        console.log('C-');
    }
    else if (number >= 75) {
        console.log('D+');
    }
    else if (number >= 75) {
        console.log('D -');
    }
    else if (number < 33) {
        console.log('F');
    }
    else
        console.log('F');

}

var number = gradePoint(90);
console.log(number);


